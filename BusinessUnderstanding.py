'''
1. Business Understanding

Tahap Business Understanding merupakan tahap awal dalam proses data science yang bertujuan untuk memahami permasalahan bisnis secara menyeluruh. Pada tahap ini, fokus utama adalah mengidentifikasi tujuan bisnis, permasalahan yang ingin diselesaikan, serta menentukan tujuan analisis data yang selaras dengan kebutuhan bisnis.

Selain itu, dilakukan penentuan ruang lingkup proyek, kriteria keberhasilan, serta asumsi dan batasan yang mungkin memengaruhi proses analisis. Hasil dari tahap ini adalah rumusan masalah yang jelas dan terukur sehingga proses data science dapat memberikan solusi yang relevan dan bernilai bagi pengambilan keputusan.

2. Data Understanding

Tahap Data Understanding bertujuan untuk memahami karakteristik data yang akan digunakan. Proses ini dimulai dengan pengumpulan data awal, dilanjutkan dengan eksplorasi data untuk mengetahui struktur, pola, dan kualitas data.

Pada tahap ini juga dilakukan identifikasi terhadap permasalahan data seperti data hilang (missing values), data tidak konsisten, outlier, dan anomali. Pemahaman data yang baik sangat penting agar proses selanjutnya dapat berjalan dengan tepat dan hasil analisis tidak menyesatkan.

3. Data Preparation

Tahap Data Preparation merupakan proses pengolahan data agar siap digunakan dalam pemodelan. Kegiatan pada tahap ini meliputi pembersihan data, penghapusan atau penanganan data yang hilang, transformasi data, normalisasi, serta pemilihan atribut yang relevan.

Tahap ini sering kali memakan waktu paling lama dalam proyek data science karena kualitas model sangat bergantung pada kualitas data yang digunakan. Output dari tahap ini adalah dataset akhir yang telah bersih dan terstruktur dengan baik.

4. Modeling

Tahap Modeling adalah proses penerapan teknik atau algoritma data science terhadap data yang telah dipersiapkan. Pada tahap ini, dipilih metode pemodelan yang sesuai dengan tujuan analisis, seperti regresi, klasifikasi, clustering, atau peramalan (forecasting).

Model kemudian dilatih menggunakan data yang tersedia dan dilakukan penyesuaian parameter agar menghasilkan performa terbaik. Dalam praktiknya, sering dilakukan beberapa percobaan model untuk memperoleh hasil yang paling optimal.

5. Evaluation

Tahap Evaluation bertujuan untuk menilai kinerja model yang telah dibangun. Evaluasi dilakukan menggunakan metrik tertentu yang sesuai dengan tujuan bisnis, seperti akurasi, error, MSE, MAPE, atau metrik lainnya.

Selain evaluasi teknis, pada tahap ini juga dilakukan penilaian apakah hasil model sudah menjawab permasalahan bisnis yang telah dirumuskan pada tahap Business Understanding. Jika hasil belum memuaskan, proses dapat kembali ke tahap sebelumnya untuk dilakukan perbaikan.

6. Deployment

Tahap Deployment merupakan tahap akhir dalam CRISP-DM, yaitu penerapan model ke dalam lingkungan nyata. Model yang telah dievaluasi dapat diimplementasikan dalam bentuk sistem informasi, aplikasi, dashboard, atau laporan yang dapat digunakan oleh pengguna akhir.

Pada tahap ini juga dilakukan monitoring terhadap kinerja model agar tetap relevan seiring berjalannya waktu. Jika terjadi perubahan kondisi bisnis atau data, proses CRISP-DM dapat diulang kembali untuk melakukan penyesuaian.
'''